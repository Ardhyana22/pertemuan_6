// arithmetic operators
let sum = 10 + 5; //penjumlahan
let sub = 10 - 5; //pengurangan
let mul = 10 * 5; //perkalian
let div = 10 / 5; //pembagian
let mod = 10 % 5; //modulus
let exp = 10 ** 5; //eksponen/peringkat
console.log(sum, sub, mul, div, mod, exp);
//comparison operators
let isEqual = 5 === "5"; // sama dengan strict
let isEqual2 = 5 == "5";  //sama dengan not strict
let isNotEqual = 10 != "10"; // tidak sama dengan 1 not strict
let isNotEqual2 = 10 !== "10"; //tidak sama dengan 2 strict
let isGreater = 10 > 5; //lebih besar
let isless = 5 < 10; //lebih kecil
let isGreaterEqual = 10 >= 10; //lebih besar sama dengan
let islessEqual = 5 <= 10;  //lebih kecil sama dengan
console.log(isEqual, isEqual2, isNotEqual, isNotEqual2, isGreater, isless, isGreaterEqual, islessEqual);